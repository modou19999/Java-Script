// Funciones
// Declaración de Funciones
// javascript

// Función tradicional
function saludar(nombre) {
  return `Hola, ${nombre}!`;
}
// Arrow function
const despedir = (nombre) => {
  return `Adiós, ${nombre}!`;
};
// Arrow function simplificada
const sumar = (a, b) => a + b;
// Llamando funciones
console.log(saludar("María")); // "Hola, María!"
console.log(sumar(5, 3)); // 8
