// Crea una función que reciba un número como parámetro 
// e imprima por consola si el número dado es par o impar.


function parImpar(num) {
    if(num % 2 === 0){
        console.log("El num es par")
    } else {
        console.log("El num es impar")
    }
}

parImpar(3)
parImpar(6)
parImpar(9)

